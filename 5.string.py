# string = sebuah series of karakter || susunan dari karakter karakter 
name = "vino arystio"
name = 'vino "arystio"'

print(name)

print(name[0]) # mengambil index dari data dari depan
print(name[-2]) # mengambil index dari data dari belakang
print(name[0:4]) # mengambil index rentang
print(name[6:]) # mengambil index dari paling awal sampai yang paling terakhir
print(name[:]) # mengambil index sampai yang paling terakhir
print(name[:4]) # mengambil index dari paling akhir sampai paling awal